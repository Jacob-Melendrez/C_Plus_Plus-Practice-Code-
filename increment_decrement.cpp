#include <iostream>
using namespace std;

int main() {

    int i = 1;
    int j = 1;

// Increment Practice 
cout << "\n";
cout << "This is the value i before post increment. " << endl;
cout << i << endl;
cout << "\n";

i++;

cout << "This is the value after the variable i after post increment of 1. " << endl;
cout << i << endl;
cout << "\n";



// Decrement practice. 
cout << "\n";
cout << "This is the value i before post decrement. " << endl;
cout << j << endl;
cout << "\n";

j--;

cout << "This is the value after the variable i after post decrement of 1. " << endl;
cout << j << endl;
cout << "\n";

return 0;
}